//=============================================================================
// ゲームモードの初期化処理
void InitGame(void)
{
	//ゲーム取得
	GAME* game = GetGame();

		//weekloop呼び出し
		InitWeekloop();
		//難易度初期化
		game->difficult = DIFFICULT_PROTO;

	return;
}

//=============================================================================
// ゲームモードの初期化処理
void UninitGame(void)
{
	UninitWeekloop();

}

//=============================================================================
// ゲームモードの最新処理
