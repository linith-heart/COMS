{
	DrawWeekloop();
}

//=============================================================================
// セット難易度関数
void SetDifficult(int difficult)
{
	//ゲーム取得
	GAME* game = GetGame();
	game->difficult = difficult;
}

//=============================================================================
// ゲーム取得関数
GAME* GetGame(void)
{
	return &g_game;
}